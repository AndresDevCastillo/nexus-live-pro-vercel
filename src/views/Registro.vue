import axios from "axios";
export default {
	name: "RegistroEvento",
	data: () => ({
		API: import.meta.env.VITE_APP_API,
		usuario: null,
		contrasena: null,
		contrasenac: null,
		key: null,
		telefono: null,
		correo: null,
		password: null,
		store: null,
		loginTxt: "Registrar Administrador",
	}),
	methods: {
		async signSAMYFLW() {
			this.loginTxt = "Cargando...";
			if (this.contrasena == this.contrasenac) {
				axios
					.post(`${this.API}/usuario/crear`, {
						usuario: this.usuario,
						contrasena: this.contrasena,
						telefono: this.telefono,
						correo: this.correo,
						key: this.key,
					})
					.then((resp) => {
						if (resp.data.creado) {
							this.$toast.add({ severity: "success", summary: "Autorización", detail: "Creado correctamente", life: 3000 });
							const router = this.$router;
							setTimeout(function () {
								router.push("/login");
							}, 3000);
						} else {
							this.$toast.add({ severity: "error", summary: "Autorización", detail: resp.data.message, life: 3000 });
						}
					})
					.catch(() => {
						this.$toast.add({ severity: "error", summary: "Autorización", detail: "Comuniquese con soporte!", life: 3000 });
					});
			} else {
				this.$toast.add({ severity: "error", summary: "Autorización", detail: "Las contraseñas no coinciden", life: 3000 });
			}
			this.loginTxt = "Registrar Administrador";
		},
	},
