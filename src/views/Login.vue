<script>
import axios from "axios";
import { useStoreEvento } from "../store";
export default {
	name: "LoginEvento",
	data: () => ({
		API: import.meta.env.VITE_APP_API,
		user: null,
		password: null,
		store: null,
		loginTxt: "Iniciar Sesión",
		redes: [
			{
				title: "WhatsApp",
				href: "https://walink.co/239ecd",
				img: "/assets/img/footer/what.png",
			},
			{
				title: "Instagram",
				href: "https://www.instagram.com/nexuslivepro",
				img: "/assets/img/footer/ins.png",
			},
			{
				title: "TikTok",
				href: "https://www.tiktok.com/@nexuslivepro",
				img: "/assets/img/footer/tiktok.png",
			},
		],
	}),
	methods: {
		async signSAMYFLW() {
			this.loginTxt = "Cargando...";
			const USER = {
				user: this.user,
				password: this.password,
			};
			await axios
				.post(`${this.API}/auth/login`, USER)
				.then((resp) => {
					if (resp.data) {
						this.store.saveUser(resp.data);
						if (this.store.evento.evento == null) {
							this.$router.push("/panel/bonus");
						} else {
							this.$router.push("EventoEspecialView");
						}
					}
				})
				.catch((error) => {
					console.log(error);
					if (error.response.status == 401) {
						this.$toast.add({ severity: "error", summary: "Autorización", detail: error.response.data.message, life: 3000 });
					}
				});
			this.loginTxt = "Iniciar Sesión";
		},
	},
	mounted() {
		this.store = useStoreEvento();
		if (this.store.isActive()) {
			this.$router.push("/panel/bonus");
		}
	},
};
</script>
